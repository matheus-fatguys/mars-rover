package com.eventmobi.matheus.marsrover.domain;

public class Plateau {

    private int topX;
    private int topY;

    public Plateau(int topX, int topY) {
        this.topX = topX;
        this.topY = topY;
    }

    public int getTopX() {
        return topX;
    }

    public int getTopY() {
        return topY;
    }
    
}
